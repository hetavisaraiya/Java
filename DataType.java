package dataType;

public class DataType {

	public static void main(String[] args) {
		byte x = 2;
		System.out.println(x);
		int age = 25;
		float rateOfIn = 2.3f;
		double rate = 3.14;
		System.out.println(age);
		System.out.println(rateOfIn);
		System.out.println(rate);
		boolean isThisCourseCool = true;
		System.out.println(isThisCourseCool);
		char myChar = '@';
		System.out.println(myChar);
	}

}
